# Задача: Анализ текстового файла
# У тебя есть текстовый файл data.txt. В нём записан большой текст.

# Твои задачи:
# Прочитать содержимое файла и вывести первые 5 строк.
# Посчитать количество слов в файле.
# Определить самое часто встречающееся слово.
# Записать в новый файл summary.txt информацию:
# Общее количество слов
# Топ-5 самых частых слов с их количеством встречаемости

# Стоп-слова (можно расширить)
stop_words = {"и", "в", "на", "с", "для", "это", "а", "по", "он", "она", "они", "из", "но", "как", "что", "к", "от", "же", "у", "за", "мы", "вы"}

with open("data.txt", "r", encoding="utf-8") as file:
    lines = file.readlines()
    print("Первые 5 строк файла:")
    for line in lines[:5]:
        print(line.strip())

    file.seek(0)  # Сбрасываем указатель файла
    words = file.read().translate(str.maketrans("", "", string.punctuation)).lower().split()

    # Фильтруем слова, удаляя стоп-слова
    words = [word for word in words if word not in stop_words]

print("\nКоличество слов в файле:", len(words))

counts = {}
for word in words:
    counts[words.count(word)] = word
most_common = counts[max(counts)]

print("Самое часто встречающееся слово:", most_common)

with open("summary.txt", "w", encoding="utf-8") as file:
    file.write(f"Общее количество слов: {len(words)}\n")
    file.write(f"Топ-5 самых частых слов:{sorted(counts.items(), reverse=True)[:5]}")

